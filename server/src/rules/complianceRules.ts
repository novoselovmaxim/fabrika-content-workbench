export function getRulesByCategory(category?: ComplianceCategory): ComplianceRule[] {
  if (category) return COMPLIANCE_RULES.filter(r => r.category === category);
  return COMPLIANCE_RULES;
}

export function getRulesForPlatform(platform: string): ComplianceRule[] {
  return COMPLIANCE_RULES.filter(r => {
    if (!r.platforms) return true;
    const p = r.platforms[platform];
    if (!p || p === "blocked") return false;
    return true;
  });
}

export function getRuleById(id: string): ComplianceRule | undefined {
  return COMPLIANCE_RULES.find(r => r.id === id);
}
