const addPost = (
  week: number,
  day: string,
  format: string,
  title: string,
  rubric: string,
  goal: string,
  hook: string,
  keyMessage: string,
  cta: string,
  status = "planned"
) => {
  const dayMap: Record<string, number> = {
    Пн: 1, Вт: 2, Ср: 3, Чт: 4, Пт: 5, Сб: 6, Вс: 7,
  };
  const startDate = new Date(2026, 5, 22); // June 22, 2026 (Monday of week 1)
  const date = new Date(startDate);
  date.setDate(date.getDate() + (week - 1) * 7 + (dayMap[day] - 1));
  const fmt = format === "Карусель" ? "carousel" :
    format === "Reels" ? "reel" :
    format === "Stories" ? "stories" :
    format === "Кейс" || format.includes("Кейс") ? "case" :
    format === "Продающий" ? "sales" :
    format === "Экспертный" ? "expert" : "post";
  const rubricId = rb(rubric);
  const contentTypeId = ct(fmt);
  postData.push({
    id: uuid(),
    title,
    rubricId,
    contentTypeId,
    scheduledDate: date.toISOString().split("T")[0],
    status,
    goal,
    hook,
    keyMessage,
    cta,
  });
};

